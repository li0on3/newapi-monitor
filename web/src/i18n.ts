export function detectLanguage(browserLanguages: readonly string[] = [], savedLanguage?: string | null): Language {
  if (savedLanguage === 'zh-CN' || savedLanguage === 'en') return savedLanguage
  const primary = browserLanguages[0]?.toLowerCase() || ''
  return primary.startsWith('zh') ? 'zh-CN' : 'en'
}

function initialLanguage(): Language {
  if (typeof window === 'undefined') return 'zh-CN'
  return detectLanguage(navigator.languages, window.localStorage.getItem(STORAGE_KEY))
}

let currentLanguage: Language = initialLanguage()

export function getLanguage(): Language {
  return currentLanguage
}

export function setLanguage(language: Language): void {
  currentLanguage = language
  if (typeof document !== 'undefined') document.documentElement.lang = language
  if (typeof window !== 'undefined') window.localStorage.setItem(STORAGE_KEY, language)
}

export function translate(source: string, language: Language = currentLanguage, variables: Record<string, string | number> = {}): string {
  const template = language === 'en' ? ENGLISH_OVERRIDES[source] || ENGLISH[source] || source : source
  return template.replace(/\{\{(\w+)\}\}/g, (_, name: string) => String(variables[name] ?? '{{' + name + '}}'))
}

export function t(source: string, variables?: Record<string, string | number>): string {
  return translate(source, currentLanguage, variables)
}

